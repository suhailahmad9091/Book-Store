    public view_order() {
        initComponents();

        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select o.order_id, o.user_id, o.book_code, b.price, o.quantity, b.price*o.quantity, o.date\n"
                    + "from orders o, books b \n"
                    + "where o.book_code = b.code;");
            while (rs.next()) {
                String order_id = rs.getString(1);
                String user_id = rs.getString(2);
                String book_code = rs.getString(3);
                String price = rs.getString(4);
                String quantity = rs.getString(5);
                String total_amount = rs.getString(6);
                String date = rs.getString(7);

                String tbData[] = {order_id, user_id, book_code, price, quantity, total_amount, date};
                DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
                tblModel.addRow(tbData);
            }
        } catch (Exception ex) {
            System.out.println("Exception : " + ex);
        }
    }

